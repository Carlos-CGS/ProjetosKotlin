fun main() {
    
    fun soma(num1: Int,num2: Int) : Int{
    return num1 + num2
	  }
    
    fun subtracao(num1: Int,num2: Int) : Int{
    return num1 - num2
	  }

    fun divisao(num1: Int,num2: Int) : Int{
    return num1 / num2
	  }

    fun multiplicacao(num1: Int,num2: Int) : Int{
    return num1 * num2
	  }

    println(soma(1,2))
    println(subtracao(5,3))
    println(divisao(10,3))
    println(multiplicacao(1,2))
   
}
